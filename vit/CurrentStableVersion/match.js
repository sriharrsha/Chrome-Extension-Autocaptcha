function matchImg(rx, ry, pix, mask) {
    flag = 1;
    breakflag = 0;
    x = 0;
    y = 0;
    count = 0;
    for (var x = 0; x < mask.length; ++x) {
        for (var y = 0; y < mask[0].length; ++y) {
            try {
                if (mask[x][y] == '1') {
                    if (pix[rx + x][ry + y] == '1') {
                        count += 1;
                    } else {
                        flag = 0;
                        breakflag = 1;
                        break;
                    }
                }
            } catch (e) {
                flag = 0;
                breakflag = 1;
                break;
            }
        }
        if (breakflag) {
            break;
        }
    }
    if (count === 0) {
        flag = 0;
    }
    return flag;
}

function skip(start, end, y) {
    flag = 0;
    for (var i = 0; i < start.length; ++i) {
        if (y >= start[i] && y <= end[i]) {
            flag = 1;
            break;
        }
    }
    return flag;
}

function sort(sorter, captcha) {
    for (var i = 0; i < sorter.length; ++i) {
        less = sorter[i];
        swap = 0;
        ls = i;
        for (var k = i; k < sorter.length; k++) {
            if (sorter[k] < less) {
                less = sorter[k];
                ls = k;
                swap = 1;
            }
        }
        if (swap) {
            temps = sorter[i];
            sorter[i] = sorter[ls];
            sorter[ls] = temps;
            tempc = captcha[i];
            captcha[i] = captcha[ls];
            captcha[ls] = tempc;
        }
    }
}


